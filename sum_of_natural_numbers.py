# Initialize sum variable to 0
sum_natural_numbers = 0

# Loop through the first 10 natural numbers
for i in range(1, 11):
    sum_natural_numbers += i  # Add the current number to the sum

# Output the result
print("The sum of the first 10 natural numbers is:", sum_natural_numbers)
