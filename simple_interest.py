# Find the Simple Interest on Rs. 200 for 5 years at 5% per year
# Given values
principal = 200  # Principal amount
rate = 5         # Rate of interest per year
time = 5         # Time in years

# Calculate Simple Interest
simple_interest = (principal * rate * time) / 100

print(f"The Simple Interest on Rs. {principal} for {time} years at {rate}% per year is Rs. {simple_interest}.")
